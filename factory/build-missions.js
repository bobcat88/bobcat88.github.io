#!/usr/bin/env bun
// build-missions.js — generate the per-mission dossier data from the library SSOT.
// Reads library/missions.json → writes ../missions/missions-data.js (window.MISSIONS),
// and copies factory/designs/base.css → ../missions/base.css so /missions is self-contained.
// The page missions/index.html reads ?id=<key> and renders the dossier client-side.
// Run after editing the JSON:  bun factory/build-missions.js
import { readFileSync, writeFileSync, copyFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = join(import.meta.dir, "..");
const src = JSON.parse(readFileSync(join(ROOT, "library/missions.json"), "utf8"));
const data = { labels: src.labels, person: src.person, missions: src.missions };

const out = `// GENERATED by factory/build-missions.js from library/missions.json — do not edit by hand.\nwindow.MISSIONS = ${JSON.stringify(data, null, 2)};\n`;
writeFileSync(join(ROOT, "missions/missions-data.js"), out);

copyFileSync(join(ROOT, "factory/designs/base.css"), join(ROOT, "missions/base.css"));

console.log("✓ Missions data →", join(ROOT, "missions/missions-data.js"), `(${Object.keys(src.missions).length} missions)`);
console.log("✓ base.css     →", join(ROOT, "missions/base.css"));
